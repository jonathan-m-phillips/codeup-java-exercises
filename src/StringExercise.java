public class StringExercise {
    public static void main(String[] args) {

        // 1: String Basics
        String stringBasics = "We don't need no education\nWe don't need no thought control";
        System.out.println(stringBasics);

        stringBasics = "\nCheck \"this\" out!, \"s inside of \"s!";
        System.out.println(stringBasics);

        stringBasics = "\nIn windows, the main drive is usually C:\\";
        System.out.println(stringBasics);

        stringBasics = "I can do backslashes \\, double backslashes \\\\,\nand the amazing triple backslash \\\\\\!";
        System.out.println(stringBasics);

    }
}
